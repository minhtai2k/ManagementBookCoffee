    private void createTable(ArrayList<NhanVien> nhanViens){
        DefaultTableModel defaultTableModel = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; //To change body of generated methods, choose Tools | Templates.
            }
            
        };      
        defaultTableModel.addColumn("Mã nhân viên");
        defaultTableModel.addColumn("Tên nhân viên");
        defaultTableModel.addColumn("Ngày sinh");
        defaultTableModel.addColumn("Giới tính");
        defaultTableModel.addColumn("Địa chỉ");
        defaultTableModel.addColumn("Số điện thoại");
        defaultTableModel.addColumn("Chức vụ");
        defaultTableModel.addColumn("ID");
        defaultTableModel.addColumn("Giới tính");
        defaultTableModel.setRowCount(0);
        for(NhanVien nhanVien : nhanViens){
            defaultTableModel.addRow(new Object[]{nhanVien.getManv(), nhanVien.getTennv(), nhanVien.getNgaysinh(), nhanVien.getGioitinh(),
                nhanVien.getDiachi(), nhanVien.getSdt(), nhanVien.getChucvu(), nhanVien.getIdcanhan(), nhanVien.getGioitinh()});   
        }
        tableQLNV.setModel(defaultTableModel);
    }
    
