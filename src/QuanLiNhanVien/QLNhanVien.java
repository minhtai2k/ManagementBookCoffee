    private void tableQLNVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableQLNVMouseClicked
        // TODO add your handling code here:
        int row = tableQLNV.getSelectedRow();
        try {
            if(row != -1){            
            txtManv.setText((String) tableQLNV.getValueAt(row, 0));
            txtTennv.setText((String) tableQLNV.getValueAt(row, 1));
            dcNgaysinh.setDate((Date) tableQLNV.getValueAt(row, 2));
            txtDiachi.setText((String) tableQLNV.getValueAt(row, 4));
            txtSdt.setText((String) tableQLNV.getValueAt(row, 5));
            String clv = (String) tableQLNV.getValueAt(row, 3);
            if(clv.equals(rdbCasang.getText()))
                rdbCasang.isSelected();
            else if(clv.equals(rdbCachieu.getText()))
                rdbCachieu.isSelected();
            else 
                rdbCatoi.isSelected();
            String cv = (String) tableQLNV.getValueAt(row, 6);
            cbbChucvu.setSelectedItem(cv);
            txtID.setText((String) tableQLNV.getValueAt(row, 7));
            String gt = (String) tableQLNV.getValueAt(row, 8);
            if(gt.equals(rdbNam.getText()))
                rdbNam.setSelected(true);
            else
                rdbNu.setSelected(true);
            NhanVien nvClick = new NhanVienDAO().timkiemtheoID((String) tableQLNV.getValueAt(row, 0));
            ImageIcon imageIcon = new ImageIcon(nvClick.getAnh());
            Image image = imageIcon.getImage();
            Image newimg = image.getScaledInstance(200, 160,  java.awt.Image.SCALE_SMOOTH);
            imageIcon = new ImageIcon(newimg);
            lbAnh.setIcon(imageIcon);            
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tableQLNVMouseClicked

