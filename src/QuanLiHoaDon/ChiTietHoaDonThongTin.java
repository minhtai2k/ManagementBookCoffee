    private void TimkiemcthdjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TimkiemcthdjButton1ActionPerformed
        // TODO add your handling code here:
        ChiTietHoaDon chiTietHoaDontimkiem = new ChiTietHoaDon();
        if(!new CheckInputThucDon().checkIDthucdon(matdjTextField2.getText()) && matdjTextField2.getText().equals(""))
            JOptionPane.showMessageDialog(rootPane, "Mã thực đơn không hợp lệ! Yêu cầu nhập lại");
        else{
            chiTietHoaDontimkiem.setMahd(mahoadonjTextField1.getText());
            chiTietHoaDontimkiem.setMatd(matdjTextField2.getText());
            try {
                chiTietHoaDonsBang.clear();
                chiTietHoaDontimkiem = new ChiTietHoaDonDAO().timtheoID2ma(chiTietHoaDontimkiem.getMahd(), chiTietHoaDontimkiem.getMatd()); 
                chiTietHoaDonsBang.add(chiTietHoaDontimkiem);
                showTable();
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_TimkiemcthdjButton1ActionPerformed

