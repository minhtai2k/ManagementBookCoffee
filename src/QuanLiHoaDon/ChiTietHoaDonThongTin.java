    private void createTable(ArrayList<ChiTietHoaDon> chiTietHoaDons){
        DefaultTableModel defaultTableModel = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; //To change body of generated methods, choose Tools | Templates.
            }
        };      
        defaultTableModel.addColumn("Mã hóa đơn");
        defaultTableModel.addColumn("Mã thực đơn");
        defaultTableModel.addColumn("Tên thực đơn");
        defaultTableModel.addColumn("Số lượng");
        defaultTableModel.addColumn("Đơn giá");
        defaultTableModel.addColumn("Giảm giá");
        defaultTableModel.addColumn("Số tiền");
        for(ChiTietHoaDon chiTietHoaDon : chiTietHoaDons){
            ThucDon tenthucDon = new ThucDonDAO().timkiemtheoID(chiTietHoaDon.getMatd());
            defaultTableModel.addRow(new Object[]{chiTietHoaDon.getMahd(), chiTietHoaDon.getMatd(), tenthucDon.getTentd(), chiTietHoaDon.getSoluong(), 
                chiTietHoaDon.getDongia(), chiTietHoaDon.getGiamgia(), chiTietHoaDon.getThanhtien()});   
        }
        CTHDTable.setModel(defaultTableModel);
    }
   
